/**
 *  for loops = repeat a loop a limited amount of times
 *              for(declare index; condition; step;){}
 */

// count up to 10
for(let i = 1; i <=10; i++){
    console.log(String(i));
}

// count down to ten
for(let i = 10; i > 0; i--){
    console.log(String(i));
}
console.log('Happy Birthday!');